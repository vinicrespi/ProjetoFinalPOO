        PessoaDTO pessoaDTO = new PessoaDTO();

       //Pegando o valor do TXT - Matricula
        pessoaDTO.setNome(jTextField1.getText());
         //passando para o DTO o resultado do BO - Procurar
        pessoaDTO = pessoaBO.procurarPorNome(pessoaDTO);
        if(pessoaDTO !=null)


           pessoaBO.alterar(carregarDTO());

    public PessoaDTO carregarDTO(){
        PessoaDTO pessoaDTO = new PessoaDTO();
        if("".equals(jTextField1.getText()))
            pessoaDTO.setNome("");
        else
            pessoaDTO.setNome(jTextField1.getText());
            pessoaDTO.setConjuge(jTextField2.getText());
            pessoaDTO.setDataNasc(Date.valueOf(jTextField3.getText()));
            pessoaDTO.setCpf(jTextField4.getText());
            pessoaDTO.setIdade(Integer.parseInt(jTextField5.getText()));
            pessoaDTO.setRg(jTextField6.getText());
        return pessoaDTO;
    }
